/**
 * - 赋值运算符:
 *     + 基础运算符: =、 ??=
 *     + 复合运算符: +=、 -=、 *=、 /=、 %=、 ~/、
 *     + 位运算
 */

void main() {
    int a = 10;
    int b = 5;

    b ??= 10;
    print(b);   // 5

    a += 2;
    print(a);   // 12

    a -= 5;
    print(a);   // 7

    a *= b;
    // a /= b;
    a ~/= b;
    a %= b;

}